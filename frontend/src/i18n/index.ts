const savedLang = localStorage.getItem('ui_language') || 'zh';

i18n.use(initReactI18next).init({
  resources,
  lng: savedLang,
  fallbackLng: 'en',
  interpolation: { escapeValue: false },
});

export default i18n;

export function setUILanguage(lang: 'zh' | 'en') {
  localStorage.setItem('ui_language', lang);
  i18n.changeLanguage(lang);
}

export function characterName(char: { name_zh: string; name_en: string }, lang: string) {
  return lang === 'zh' ? char.name_zh : char.name_en;
}
